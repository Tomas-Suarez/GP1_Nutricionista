public void llenartablaRenglon (List<RenglonDeMenu> Renglon){
    var forma = new String[]{"Alimento","Gramos","subt.calorias","horario"};//define la forma de la tabla
    DefaultTableModel modelo = new DefaultTableModel(forma,0);
    Trenglones.setModel(modelo);
    
    if (Renglon ==null){
       
        Renglon = dataR.buscarpormenu((Integer)men.getCodMenu());
        }
            
    for (var renglon:Renglon){
    modelo.addRow(new Object[]{
        renglon,
        renglon.getAlimento().getNombre(),
        renglon.getCantidadGrs(),
        renglon.getSubTotalCalorias(),
        renglon.getHorario()    
    });
        
    }
    llenaralimentos();
}

public void llenaralimentos (){
    var forma = new String[]{"Nombre","TipoComida","Calorias x100g","Detalle"};
    DefaultTableModel modelo = new DefaultTableModel(forma,0);
    Talimento.setModel(modelo);
    
    for (var alimento:dataA.listarAlimentos()){
         modelo.addRow(new Object[]{
                alimento,
                alimento.getTipoComida(),
                alimento.getCaloriasPor100g(),
                alimento.getDetalle(),
                alimento.getBaja() ? "No activo" : "Activo" //Si es TRUE, lo coloca en "No activo", y si es FALSE lo coloca en "Activo"
            });
    }
    
}



public Alimento getAlimentSelected () {
    Alimento alimento = null;
    try {
    var y = Talimento.getSelectedRow();
    y= y<0?0:y;
    alimento = (Alimento)Talimento.getValueAt(y, 0);
    } catch (Exception ex){}
    return alimento;       
}

public void cargarRenglon (){
    RenglonDeMenu ren = new RenglonDeMenu(1, 1, getAlimentSelected(), men, "s");
    
    dataR.agregarRen(ren);
    llenartablaRenglon(null);
}

