    private void crear() {
        Window window = SwingUtilities.getWindowAncestor(this);
        if (validarFormulario()) {
            if (jButton1.getText().equals("Guardar")) {
                Alimento ali = new Alimento(
                        Tnombre.getText(),
                        (String) jcComida.getSelectedItem(),
                        (Integer) Scalorias.getValue(),
                        Tdetalle.getText(),
                        false
                );

                data.guardarAlimento(ali);
                window.dispose(); //Cerrar la ventana
            } else {
                alimento.setNombre(Tnombre.getText());
                alimento.setTipoComida((String) jcComida.getSelectedItem());
                alimento.setCaloriasPor100g((Integer) Scalorias.getValue());
                alimento.setDetalle(Tdetalle.getText());
                alimento.setBaja(false);

                data.actualizarAlimento(alimento);
                window.dispose(); //Cerrar la ventana
            }
            lalo.TablaAlimento();
        }
    }

    //Verificamos si los campos estan llenos
    private boolean validarFormulario() {
        //validar nombre
        if (Tnombre.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "El campo de nombre no debe estar vacío!");
            return false;
        }
        //validar detalles
        if (Tdetalle.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "El campo de detalle no debe estar vacío!");
            return false;
        }
        return true;
    }

